package basic;
//1.Arithmetic Operators->+,-,*,/,%
public class Operators {
	public static void main(String[] args) {
		int i=10,j=3,k;
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i*j);
		System.out.println(i/j);
		System.out.println("% = "+(i%j));
		k = i+j;
		System.out.println("k = "+k);
		double a,r=4;
		a = Math.PI*r*r;
		System.out.println("area of circle = "+a);
	}
}
